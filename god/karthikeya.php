			<div class="box-content"><h4>1. The Story of Kartikeyas Birth</h4><br>
<p>A demon named Tarakasura was terrorizing the land, and even the gods could not stop him. They knew there was only one hope: the birth of a great divine warrior who could save them. This might sound like the plot of a fantasy book or movie, but this is actually the legend behind the birth of Kartikeya, the warrior god of Hinduism. His legends, the circumstances of his birth, and even his name vary by region. Let's delve a bit further into Kartikeya's origin story and the meanings behind his many names.</p>

<p>According to Hindu legend, Shiva, the destroyer god of Hinduism, was destined to have a son who would be a great warrior and save the gods. In a version of Kartikeya's origin story commonly found in northern India, Shiva and his wife, Parvati, created a being with five faces. A glowing spark emerged from each face. Shiva sent the five-faced being into the holy Ganges River. The five sparks floated out, and each formed into a child. Young women found them in the forest pond, each taking and nursing one of the children.</p>

<p>When Shiva and Parvati eventually took their children back, Parvati did not know how she could manage to take care of and feed all five. To make her job easier, Shiva merged the children into one being with six heads: one for each of the children, representing the five senses, and a final head to combine the senses into one, symbolic of the mind and power of Shiva. The six-headed child was the much-awaited warrior god, Kartikeya.</p><br>
<h4>2. Kartikeya became the Guru</h4><br>
<p>Once he asked Lord Brahma to explain the meaning of Om. Brahma explained to him but he was not satisfied. Later on, when asked by Lord Shiva, he explained the whole episode to him.  Lord Shiva told that he must learn from Lord Brahma, as he is the supreme creator. To this Kartikeya replied, ‘Then you tell me, what is the meaning of Om?’ Hearing this, Lord Shiva smiled and said, ‘Even I don’t know.’ Kartikeya then said, ‘Then I will tell you because I know the meaning of Om.’</p>

<p>‘Then tell me the meaning since you know it’, said Lord Shiva.</p>
<p>‘I can’t tell you like this. You have to give me the place of the Guru. Only if you put me on the pedestal of the Guru can I tell you’, said Kartikeya. Guru means he has to be on a higher position or platform. The teacher has to sit on a higher place and the student has to sit down and listen to him.</p>

<p>How can Lord Shiva find a seat higher than him, for He is the highest and greatest of Gods? So then Lord Shiva lifted the young Kartikeya on to His shoulders. And then in the ear of Lord Shiva, Lord Kartikeya explained the meaning of the Pranava Mantra (Om).</p>
<p>Kartikeya explained that the entire Creation is contained in Om. The Trinity – Lord Brahma, Lord Vishnu and Lord Shiva are contained in Om. This is the essence and also the secret of Om that Lord Kartikeya narrated to Lord Shiva.</p>

<p>Upon hearing this, Goddess Parvati (Mother of Lord Kartikeya, and an incarnation of the Mother Divine) was elated and overcome with joy.</p>

<p>She said, ‘You have become a Guru (Swami) to my Lord (Natha)!’ Saying this she addressed her son as Swaminatha, and ever since Lord Kartikeya also came to be known as Swaminatha.</p>

