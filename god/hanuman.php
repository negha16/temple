<p>On his way he saw Rahu trying to devour the sun and thus cause an eclipse. Mistaking Rahu to be a worm, Hanuman dashed towards him, attempting to catch him. Rushing for his life, Rahu sought shelter in the refuge of Indra, the lord of the skies. Indra picked up his deadly thunderbolt, mounted his white elephant named Airavata and made off in search of Hanuman, seeking to restrain his seeming impudence. The clouds rumbled and lightning thundered across the vast skies in an expression of Indra's wrath. But neither this scary scenario, nor the mightily armed Indra on his high mount, was sufficient enough to induce even a trace of fear in the heart of Hanuman. On the contrary, the spectacle only served to fuel his excitement and mistaking Airavata for a toy, he made a grab for the pachyderm, seized its trunk and leapt on its back. Taken aback by the child's spirited and playful defiance, Indra stuck at Hanuman with his thunderbolt, and the wound thus inflicted hurtled him speedily down to the earth. His father Vayu immediately sprung to his rescue and caught him in mid air.</p>
<p>The sight of his beloved son lying helpless in his arms infuriated the wind-god. He drew in a mighty breath and sucked away all the air from the cosmos. "Let all those who have harmed my son choke to death," he thought out aloud. Predictably there was panic in the cosmos. Without air, life on every level was threatened. The gods, realizing their folly, went in unison to Vayu and asked for his forgiveness. To make amends they showered the following blessings and powers on the child:</p>
