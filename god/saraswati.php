				<h4>Saraswati, the river goddess</h4>

<p>Saraswati is also depicted as a river goddess in some texts. The Saraswati river symbolizes purity. There is a story that explains how Saraswati became a river. Once, there was a terrible war between the Bhargavas and Hehayas. During the battle, an all-consuming fire called Vadavagni was born, which had the power to destroy the whole world. The worried devas went to Shiva, who suggested that they should ask Saraswati for help so that she could become a river and immerse the Vadavagni in the sea. So they went to Saraswati and begged her to protect the universe. She replied that she would do it only if her husband, Brahma, allowed her. Hence, they went to Brahma. On hearing of the problem, Brahma told Saraswati to turn into a river. Saraswati then left Brahmaloka and went to Sage Uttanka’s ashram, where she met Shiva. He handed over the Vadavagni in a pot to Saraswati and told her to emerge as a river from a Plaksha tree. Accordingly, Saraswati merged with the tree and became a river. She flowed towards Pushkar from there. When Saraswati reached the end of her journey to the ocean, she immersed the fire in the sea.</p><br>
<h4>Saraswati’s hamsa</h4>

<p>A swan or hamsa is sometimes seen near her feet. In Hindu mythology, the swan is a sacred bird that symbolizes spiritual perfection, transcendence, and moksha. When it is offered a mixture of milk and water, it drinks the milk alone. Hence, it represents the ability to discriminate between good and evil, essence from the outward appearance, and the eternal from the evanescent. Saraswati is also called Hamsavāhini, which means “she who has a hamsa as her vehicle”.</p>
 
