<p>Kamsa, an evil prince, imprisoned his father to usurp the throne. As a punishment, it was prophesied that his sister’s eighth child would be the reason for his downfall. On hearing this, he threw his sister Devaki and her husband Vasudeva in a dungeon on their wedding day. The evil Kamsa killed each and every child of Devaki’s. It was by God’s grace that their seventh child, Balram was saved by being taken into Rohini’s womb.</p>

<p>The eighth child was born on a moonless stormy night. After the birth of the baby boy, Vasudeva realised that the gates of their prison were opened and all the guards were fast asleep. A divine voice advised Vasudeva to carry the child, Krishna, in a basket and walk into the water. As soon as Vasudeva stepped into the river, the water level of the river subsided allowing him to wade through the water to Gokula. A serpent protected Krishna from the rain with its large hood.</p>

<p>On reaching Gokula, Vasudeva left Krishna in Nanda’s house with Nanda’s wife, Yashoda. Vasudeva took Yashoda’s newborn baby girl back to the dungeon. When Kamsa heard about the birth of Devaki’s eighth child, he stormed into the dungeon. He snatched the baby from them. The baby slipped and in a flash of light took the form of Goddess Durga. The Goddess broke the news to Kamsa that Krishna is in safe hands and that his doom is nearing.</p>
<h4>Krishna and his love for butter</h4>
<p>The village that Krishna lived in, Gokula, was a land of ‘Gopalas’ or cattle herders. So, there was an abundance of milk, curds, and butter in the village. Krishna was very fond of butter and would make use of every opportunity to steal a pot of butter from his mother or from any of the mothers in the village. All the mothers, or ‘Gopis’ as they were called, started tying pots of butter to the ceiling so that Krishna or his friends couldn’t reach them.Krishna would team up with his friends to reach these pots tied high up to the ceiling too. The would climb up to the roof and move the ceiling tiles to get the pot of butter or climb on each other’s shoulders to create a human ladder and steal the butter. If none of the tricks worked, they would hurl a pebble at the pot and take turns to catch the butter with their open mouths.</p>

<p>The Gopis found out that Krishna was the prime culprit for stealing the butter and hence complained to his mother, Yashoda. Yashoda apologised to the women and promised to discipline Krishna. Mischievous little Krishna decided to play a prank on the Gopis. When the women had all gone to the river to bathe, he went and stole all their clothes from the bank of the river. He said he would return their clothes only if they promised that they would stop complaining to his mother.Yashoda heard about this and unable to discipline Krishna, tied him up to a heavy staff. Krishna couldn’t release himself so he decided to go by the river where his friends would help him. He started walking through the forest when he got stuck between two trees that were growing very close to one another. 
Krishna simply tugged hard enough to move and ended up uprooting the trees. Yashoda ran to the site to see if her little boy was hurt but saw how he had felled two trees so easily. This incident made her realise more about Krishna’s extraordinary powers.</p>
