<p>Lord Vishnu takes the form of Narasimha in his fourth incarnation, the previous one being that of a Boar (Varaha). Vishnu kills the demon Hiranyaksha during his Varaha avatar.</p>

<p>Hiranyaksha’s brother Hiranyakashipu wants to take revenge by destroying Lord Vishnu and his followers. He performs penance to please Brahma, the god of creation. Impressed by this act, Brahma offers him anything he wants.</p>

<p>Hiranyakashipu asks for a tricky boon. That he would not die either on earth or in space; nor in fire nor in water; neither during day nor at night; neither inside nor outside (of a home); nor by a human, animal or God; neither by inanimate nor by animate being.</p>

<p>Brahma grants the boon. With virtually no fear of death he unleashes terror. Declares himself as god and asks people to utter no god’s name except his. However his son Prahlada (who a devoted worshiper of Lord Vishnu!) refuses. Repeated pressurization on him yields no results for Hiranyakashipu. Prahlada declares the omnipresence of Lord Vishnu.</p>

<p>Once Hiranyakashipu points to a pillar and asks if Vishnu is present in it. Prahlada nods in affirmative. Angered at it, he draws his sword and cuts the pillar; Narasimha appears out of the broken pillar.</p>

<p>Narasimha (being a man-lion god form) kills Hiranyakashipu. He comes out to kill at the twilit (neither day nor night);on the doorsteps of his palace (neither inside nor outside); uses his nails to kill (neither animate nor inanimate); puts him on his lap before killing (neither earth nor in space). Thus making power of the boon ineffective.</p>

<p>The death of this demon king is celebrated as Holi (the celebration of colors) in India, especially in the northern parts.</p>

