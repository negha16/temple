			<div class="box-content"><h4>1. The Story of Ganeshas Birth</h4><br>
<p>Lord Shiva and Goddess Parvati would stay on Mount Kailash, making it their abode. Most of the time, Shiva would be out fulfilling other responsibilities while Parvati was alone at the mountain.</p>

<p>One day, on such a similar occasion, Parvati had to go to take a bath and did not wish to be disturbed by anyone at all. Parvati ended up making a statue of a child from turmeric and breathed life into him. She called the child Ganesha, and he was absolutely loyal to her. She asked him to guard the home while she took a bath. Yet again, Shiva showed up and proceeded to enter the house. But this time, he was stopped by Ganesha who refused to move aside. Shiva did not know who this unknown child was so he asked his forces to destroy the child. But Ganesha had the powers bestowed on him by Parvati and defeated Shiva’s army. Shiva, known for his extreme fury, lost control on his temper and ended up beheading Ganesha.</p>

<p>When Parvati stepped out and saw the dead body of her creation, her fury knew no bounds. She lashed out at Shiva and threatened to destroy the entire universe as a consequence of those actions. Now, the universe was the responsibility of Bramha, Vishnu, and Shiva. Brahma witnessed the wrath of Parvati and apologized on behalf of Shiva to her, advising her to not destroy the universe. Parvati relented on the conditions that Ganesha be brought back to life and be worshipped as the primary God. Shiva, too, realized the mistake he committed in his rage and apologized to Parvati. He advised his troops to go into the forest and get the head of the first animal they spot. Incidentally, they came across an elephant and brought back his head. This was then placed over the body and Shiva brought him to life, also accepting him as his own son. This is how Ganesha was born as we know and is now worshipped as the god of gods.</p><br>
<h4>2. The Story Of Ganesha’s Wisdom</h4>


<p>Ganesha had a elder brother called as Karthikeya. Both would get along well but, just like all other siblings, they would have moments of arguments and fights. On one such day, Ganesha and Karthikeya both ended up finding a unique fruit in the forest and grabbed it together. They refused to share it with each other and started claiming the fruit for themselves</p>

<p>When they reached Mount Kailash and presented this predicament to Shiva and Parvati, Shiva made a proposition. He recognized the fruit and said that this fruit is known to grant immortality and extensive knowledge when eaten by the rightful bearer of it. To choose who gets it, Shiva proposed a challenge. He asked Ganesha and Karthikeya to circumvent their world 3 times. Whoever would do so first and return to Mount Kailash, would be the rightful owner of the fruit.</p>

<p>Karthikeya immediately hopped onto his pet peacock and flew speedily to complete three revolutions across the Earth. Ganesha was a little stocky compared to Karthikeya and his pet was a rat who couldn’t fly. Having listened to Shiva’s proposal properly, Ganesha started walking around Shiva and Parvati and completed three circles around them. When asked by Shiva, Ganesha replied that Shiva had asked them to circumvent their world. And for Ganesha, his parents were more than the world. They were the entire universe.</p>

<p>Shiva was touched and impressed by Ganesha’s wisdom and saw him as the rightful owner of the fruit.</p>

